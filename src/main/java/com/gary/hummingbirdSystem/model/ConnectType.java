package com.gary.hummingbirdSystem.model;

/**
 * 连接类型实体类
 * 
 * @author Gary
 * 
 * @date 2017年5月11日
 */
public class ConnectType {
	private int id;
	private String name;

	public ConnectType() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
